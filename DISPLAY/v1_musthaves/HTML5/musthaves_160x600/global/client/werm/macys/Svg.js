var Svg = Class.extend({
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	//private var
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	//---------------------------------------------------------
	//Boolean
	//---------------------------------------------------------
	comb_complete:false,
	//---------------------------------------------------------
	//String
	//---------------------------------------------------------
	output:undefined,
	id: "shell",
	//---------------------------------------------------------
	//Object
	//---------------------------------------------------------
	obj:undefined,
	//---------------------------------------------------------
	//Number
	//---------------------------------------------------------
	width:undefined,
	height:undefined,
	//---------------------------------------------------------
	//$
	//---------------------------------------------------------
	$elm:undefined,
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	//methods
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	init:function($elm,obj) {
		if ($elm != undefined) {
			this.obj = obj;
			if ($elm.length == 0) ThrowError.go(this, "$elm does not exist, please make sure element exist in DOM.");
			//---------------------------------------------------
			this.$elm = $elm;
			//---------------------------------------------------
			SetPublicProp.go(this,obj);
			//---------------------------------------------------
			var origDisplay = $(".content").css("display");
			//---------------------------------------------------
			$(".content").css("display", "block");
			if (this.width == undefined) this.width = $(".content").width();
			if (this.height == undefined) this.height = $(".content").height();
			//---------------------------------------------------
			$(".content").css("display", origDisplay);
			this.start();
			//---------------------------------------------------
			this.output_CFG();	
			//---------------------------------------------------	
			this.$elm.html(this.output);
			//---------------------------------------------------
		}
	},
	//------------------------------------------------------------------------------------- 
	comb:function() {
		if (!window[this.classID].comb_complete) {
			window[this.classID].comb_complete = true;
			for (var i = 0; i < $("."+this.classID).length; i++) {
				window[this.classID].go($($("."+this.classID)[i]));
			}
		}
	},
	//------------------------------------------------------------------------------------- 
	start:function() {
	},
	//------------------------------------------------------------------------------------- 
	output_CFG:function() {
	}
});
